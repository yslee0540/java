package 조건문;

public class if문1 {

	public static void main(String[] args) {
		int x = 0;
		
		//비교연산자로 비교 가능한 것은 기본데이터만
		if (x == 0) {
			System.out.println("내가 실행되겠지요?");
		} else {
			System.out.println("나는 조건이 맞지 않을 떄 실행되는거야.");
		}
		if (x == 1) {
			System.out.println("나는 실행될까요?");
		}
		
	}

}
